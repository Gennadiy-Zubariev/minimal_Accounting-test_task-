"""
============================================================================
chart.py — ПЛАН РАХУНКІВ І ПРАВИЛА ПРОВЕДЕННЯ
============================================================================

ЩО ЦЕ ЗА ФАЙЛ:
    Це "мозок" бухгалтерії. Тут записано:
    1) Які бувають рахунки (1000 Готівка, 4000 Дохід, ...).
    2) Які бувають типи операцій (продаж, купівля, оплата, ...).
    3) Як кожен тип операції проводиться: який рахунок у Дебет (Дт),
       який у Кредит (Кт).

ДЕ ПРАВИТИ ЩО:
    • Хочу ДОДАТИ НОВИЙ РАХУНОК (напр. 1200 Товари)
      → додати рядок у список ACCOUNTS нижче.

    • Хочу ДОДАТИ НОВИЙ ТИП ОПЕРАЦІЇ (напр. "повернення товару")
      → додати рядок у OPERATION_TYPES + запис у POSTING_RULES.

    • Хочу ЗМІНИТИ, які рахунки використовує операція
      → знайти її в POSTING_RULES і поміняти "dr" та/або "cr".

ТЕРМІНИ:
    Дт (дебет)  — ліва колонка проводки.
    Кт (кредит) — права колонка проводки.
    Подвійний запис — кожна операція одночасно записується в Дт одного
                      рахунку і в Кт іншого на ту саму суму.
============================================================================
"""


# ---------------------------------------------------------------------------
# 1. ПЛАН РАХУНКІВ
# ---------------------------------------------------------------------------
# Словник: код рахунку → (назва, тип).
#
# Тип рахунку потрібен, щоб правильно рахувати сальдо і будувати P&L:
#   "asset"     — актив     (нормальне сальдо дебетове: зростає в Дт)
#   "liability" — пасив     (нормальне сальдо кредитове: зростає в Кт)
#   "equity"    — капітал   (нормальне сальдо кредитове)
#   "income"    — дохід     (нормальне сальдо кредитове, іде в P&L як +)
#   "expense"   — витрата   (нормальне сальдо дебетове, іде в P&L як −)
#
# Рахунок 3000 "Капітал" доданий, щоб початкові залишки могли дотриматись
# подвійного запису (якщо вводите готівку 50 000 — інша нога йде в Кт 3000).
ACCOUNTS = {
    "1000": {"name": "Готівка",                        "type": "asset"},
    "1100": {"name": "Дебіторська заборгованість",     "type": "asset"},
    "2000": {"name": "Кредиторська заборгованість",    "type": "liability"},
    "3000": {"name": "Капітал / Нерозподілений прибуток", "type": "equity"},
    "4000": {"name": "Дохід",                          "type": "income"},
    "5000": {"name": "Витрати",                        "type": "expense"},
}


# ---------------------------------------------------------------------------
# 2. ТИПИ ОПЕРАЦІЙ (людські назви для випадаючого списку в UI)
# ---------------------------------------------------------------------------
# Ключ — технічний код, який пишеться в БД.
# Значення — текст, який бачить користувач у формі.
OPERATION_TYPES = {
    "sale_cash":          "Продаж за готівку",
    "sale_credit":        "Продаж у кредит (клієнт винен)",
    "customer_payment":   "Оплата від клієнта",
    "purchase_cash":      "Купівля за готівку",
    "purchase_credit":    "Купівля у кредит (ми винні)",
    "supplier_payment":   "Оплата постачальнику",
    "opening_balance":    "Початковий залишок",
}


# ---------------------------------------------------------------------------
# 3. ПРАВИЛА ПРОВЕДЕННЯ — серце системи подвійного запису
# ---------------------------------------------------------------------------
# Для кожного типу операції кажемо:
#   "dr" — код рахунку, куди піде сума в Дебет
#   "cr" — код рахунку, куди піде сума в Кредит
#   "needs_counterparty" — True, якщо операція має сенс лише з контрагентом
#                          (напр. оплата від клієнта неможлива "нічия")
#
# opening_balance тут має None, бо для нього Дт/Кт визначаються на льоту
# залежно від того, на який саме рахунок вводять залишок
# (дивись функцію build_posting() нижче).
POSTING_RULES = {
    # Продаж за готівку: +Дт 1000 (гроші прийшли), +Кт 4000 (визнано дохід).
    "sale_cash":        {"dr": "1000", "cr": "4000", "needs_counterparty": False},

    # Продаж у борг: +Дт 1100 (клієнт винен), +Кт 4000 (дохід).
    "sale_credit":      {"dr": "1100", "cr": "4000", "needs_counterparty": True},

    # Клієнт заплатив: +Дт 1000 (гроші), −Кт 1100 (закриваємо його борг).
    "customer_payment": {"dr": "1000", "cr": "1100", "needs_counterparty": True},

    # Купівля за готівку: +Дт 5000 (витрата), −Кт 1000 (гроші пішли).
    "purchase_cash":    {"dr": "5000", "cr": "1000", "needs_counterparty": False},

    # Купівля в борг: +Дт 5000 (витрата), +Кт 2000 (ми винні).
    "purchase_credit":  {"dr": "5000", "cr": "2000", "needs_counterparty": True},

    # Ми заплатили постачальнику: −Дт 2000 (закрили борг), −Кт 1000 (гроші пішли).
    "supplier_payment": {"dr": "2000", "cr": "1000", "needs_counterparty": True},

    # Початковий залишок: обробляється в build_posting() окремо.
    "opening_balance":  None,
}


# ---------------------------------------------------------------------------
# 4. ФУНКЦІЯ ПРОВЕДЕННЯ: операція → пара (Дт, Кт)
# ---------------------------------------------------------------------------
def build_posting(op_type: str, opening_balance_account: str = None):
    """
    Повертає пару (debit_account, credit_account) для операції.

    Викликається з services.py перед записом у БД.

    Параметри:
        op_type: код операції, напр. "sale_cash" (з OPERATION_TYPES).
        opening_balance_account: потрібен ТІЛЬКИ для opening_balance —
                                 це код рахунку, по якому вводимо залишок
                                 (1000 / 1100 / 2000).

    Повертає:
        ("1000", "4000")  — тобто "Дт 1000, Кт 4000".

    Кидає ValueError, якщо тип невідомий.
    """
    # Особливий випадок: початковий залишок.
    # Залежно від того, актив це чи пасив, "ноги" проводки різні:
    #   1000 Готівка (актив)    → Дт 1000, Кт 3000 (Капітал)
    #   1100 Дебіторка (актив)  → Дт 1100, Кт 3000
    #   2000 Кредиторка (пасив) → Дт 3000, Кт 2000
    if op_type == "opening_balance":
        if opening_balance_account is None:
            raise ValueError("Для opening_balance треба вказати opening_balance_account")
        acc_type = ACCOUNTS[opening_balance_account]["type"]
        # Актив або витрата — піде в Дт; інша нога = 3000.
        # Пасив або капітал — піде в Кт; інша нога = 3000.
        if acc_type in ("asset", "expense"):
            return (opening_balance_account, "3000")
        else:
            return ("3000", opening_balance_account)

    # Звичайні операції — беремо з таблиці правил.
    rule = POSTING_RULES.get(op_type)
    if rule is None:
        raise ValueError(f"Невідомий тип операції: {op_type}")
    return (rule["dr"], rule["cr"])


# ---------------------------------------------------------------------------
# 5. ДОПОМІЖНЕ: чи вимагає тип операції контрагента
# ---------------------------------------------------------------------------
def needs_counterparty(op_type: str) -> bool:
    """
    True, якщо для цього типу операції обов'язково вказати контрагента.
    Використовується в UI (чи показувати пікер контрагента).
    """
    rule = POSTING_RULES.get(op_type)
    if rule is None:
        return False  # opening_balance керується окремо в UI
    return rule["needs_counterparty"]